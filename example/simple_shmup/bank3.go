var bgpal = []gb.UINT16{
	cgb.RGB(0, 0, 0), cgb.RGB(10, 10, 10), cgb.RGB(20, 20, 20), cgb.RGB(30, 30, 30),
}
var spritepal = []gb.UINT16{
	cgb.RGB(0, 0, 0), cgb.RGB(10, 10, 10), cgb.RGB(20, 20, 20), cgb.RGB(30, 30, 30),
}

func DrawTitleScreen() {
	gb.WaitVBLDone()

	gb.DisplayOff()

	gb.SetBkgData(0, 255, asset.BGTitleTiledata)
	gb.SetBkgTiles(0, 0, 20, 18, asset.BGTitleTilemap)

	gb.SHOW_BKG()
	gb.HIDE_WIN()
	gb.HIDE_SPRITES()
	gb.WaitVBLDone()
	gb.DISPLAY_ON()
	gb.Waitpad(255)
}

func LoadGameTiles() {
	gb.WaitVBLDone()
	gb.DisplayOff()

	gb.SetBkgData(0, 1, asset.BGTile)
	gb.SetBkgTiles(0, 0, 32, 32, gamemap)
	cgb.SetBkgPalette(0, 1, bgpal)
	gb.SetSpriteData(0, 3, asset.SpriteTiles)
	gb.SetSpriteTile(0, 0)
	gb.SetSpriteTile(1, 1)
	gb.SetSpriteTile(2, 2)

	gb.SPRITES_8x8()
	gb.SHOW_SPRITES()
	gb.SHOW_BKG()
	gb.DISPLAY_ON()
}
